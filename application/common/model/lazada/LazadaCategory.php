namespace app\common\model\lazada;


use think\Model;

class LazadaCategory extends Model
{
    protected $autoWriteTimestamp=true;
    protected function initialize()
    {
        parent::initialize(); // TODO: Change the autogenerated stub
    }
    public function setHasChildrenAttr($v){
        if($v){
            return 1;
        }else{
            return 0;
        }
    }
    public static function categoryNameTree($category_id,&$name=''){
        $category = self::where('category_id',$category_id)->find();
        if($category){
            $name= $category['category_name'].">>".$name;
            self::categoryNameTree($category['parent_id'],$name);
        }
    }
}